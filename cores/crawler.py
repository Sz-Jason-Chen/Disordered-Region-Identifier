@retry(wait_fixed=1000)
def get_pdb_params(protein: str) -> dict:
    """
    Get PDBs' identifiers, their valid regions and the differences between the PDB sequence and the Uniprot sequence
    :param protein: accession number
    :return: PDBs' parameters
    """
    url = f'https://www.ebi.ac.uk/pdbe/api/mappings/{protein}'
    headers = {"user-agent": random.choice(USER_AGENT_POOL)}
    response = requests.get(url, headers=headers, timeout=30)
    o_dict = json.loads(response.text)
    params = {}
    for key, value in o_dict[protein]['PDB'].items():
        sections = []
        for section in o_dict[protein]['PDB'][key]:
            chain = section['chain_id']
            unp_start = section['unp_start']
            unp_end = section['unp_end']
            if section['start']['author_residue_number']:
                residue_start = section['start']['author_residue_number']
            else:
                residue_start = section['start']['residue_number']
            if section['end']['author_residue_number']:
                residue_end = section['end']['author_residue_number']
            else:
                residue_end = residue_start + unp_end - unp_start
            sections.append({'chain': chain,
                             'unp_start': unp_start, 'unp_end': unp_end,
                             'residue_start': residue_start, 'residue_end': residue_end})
        params[key] = sections
    # Eg. pdb_paras{'5i2n':['chain':'B', 'unp_start':394, 'unp_end':544, 'residue_start':3, 'residue_end':153]}
    return params


@retry(wait_fixed=1000)
def get_amino_sequence(protein: str) -> str or None:
    """
    Get the protein's amino acid sequence, mainly for measuring the length
    :param protein:  accession number
    :return:
    """
    url = f'https://alphafold.ebi.ac.uk/api/prediction/{protein}'
    headers = {"user-agent": random.choice(USER_AGENT_POOL)}
    response = requests.get(url, headers=headers, timeout=30)
    if response.status_code == 404:
        return None
    o_dict = json.loads(response.text)
    sequence = o_dict[0]['uniprotSequence']
    return sequence


@retry(wait_fixed=1000)
def download_pdb(protein: str, identifier: str, folder: str = './pdbs/') -> str:
    """
    Download the PDB file
    :param protein: accession number
    :param identifier: identifier of the pdb
    :param folder: folder path saving downloaded files
    :return: path of the downloaded PDB file
    """
    file_path = f'{folder}{protein}_{identifier}.pdb'
    if os.path.exists(file_path):
        print(f'Checked {protein}_{identifier}.pdb')
    else:
        print(f'Downloading {protein}_{identifier}.pdb')
        url = f'https://www.ebi.ac.uk/pdbe/entry-files/download/pdb{identifier}.ent'
        headers = {"user-agent": random.choice(USER_AGENT_POOL)}
        response = requests.get(url, headers=headers, timeout=30)
        with open(file_path, 'wb') as f:
            f.write(response.content)
    return file_path


@retry(wait_fixed=1000)
def download_alphafold_pdb(protein: str, folder: str = './pdbs/') -> str:
    """
    Download the AlphaFold file
    :param protein: accession number
    :param folder: folder path saving downloaded files
    :return: path of the downloaded file
    """
    file_path = f'{folder}{protein}_alphafold.pdb'
    if os.path.exists(file_path):
        print(f'Checked {protein}_alphafold.pdb')
    else:
        print(f'Downloading {protein}_alphafold.pdb')
        data_url = f'https://alphafold.ebi.ac.uk/api/prediction/{protein}'
        headers = {"user-agent": random.choice(USER_AGENT_POOL)}
        data_response = requests.get(data_url, headers=headers, timeout=30)
        o_dict = json.loads(data_response.text)
        download_url = o_dict[0]['pdbUrl']
        download_response = requests.get(download_url)
        with open(file_path, 'wb') as f:
            f.write(download_response.content)
    return file_path


def access_protein_files(protein: str, folder: str = './pdbs/') -> list:
    """
    Download all available files by multithreading
    :param protein: accession number
    :param folder: folder path saving downloaded files
    :return: path list of the downloaded files
    """
    file_paths = []
    identifiers = list(get_pdb_params(protein=protein).keys())

    # divide PDB identifiers into groups for multithreading download
    pooled_identifiers = [identifiers[i:i + 100] for i in range(0, len(identifiers), 100)]
    # a single thread: download and save
    def single_thread(identifier):
        file_paths.append(download_pdb(protein=protein, identifier=identifier, folder=folder))
    # multithreading by groups
    for p_identifiers in pooled_identifiers:
        pools = len(p_identifiers)
        threads = [None] * pools
        # init threads and start
        for i in range(pools):
            threads[i] = threading.Thread(target=single_thread, kwargs={'identifier': p_identifiers[i]})
            threads[i].start()
        # main thread wait for all files to finish downloading
        for i in range(pools):
            threads[i].join()
    file_paths.append(download_alphafold_pdb(protein=protein, folder=folder))
    return file_paths
